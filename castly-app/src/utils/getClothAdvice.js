export function getClothAdvice(weatherData) {
  if (!weatherData) return "Загрузка данных для совета...";

  const temp = weatherData.temperature_2m;
  const apparentTemp = weatherData.apparent_temperature; 
  const wind = weatherData.wind_speed_10m;
  const isRain = weatherData.precipitation > 0;
  const isNight = weatherData.is_day === 0;

  // Шаблон 1: Одежда по температуре воздуха
  let phrase1 = "";
  if (temp <= -10) {
    phrase1 = "Сегодня экстремально холодно: обязательно надевайте термобельё, пуховик и тёплые варежки.";
  } else if (temp <= 0) {
    phrase1 = "Градусник ниже нуля: выбирайте зимнюю куртку, шапку и шарф, чтобы не замерзнуть.";
  } else if (temp <= 10) {
    phrase1 = "Погода зябкая: плотный свитер или худи в сочетании с непродуваемой курткой будут идеальны.";
  } else if (temp <= 18) {
    phrase1 = "Свежо, но приятно: лёгкого свитшота или ветровки поверх футболки будет достаточно.";
  } else if (temp <= 25) {
    phrase1 = "Тепло и комфортно: смело надевайте футболку или лёгкую рубашку.";
  } else {
    phrase1 = "Летняя жара: шорты и футболка — лучший выбор.";
  }

  // Шаблон 2: Корректировка по осадкам, ветру и времени суток
  let phrase2 = "";
  if (isRain) {
    phrase2 = "Не забудьте зонт или дождевик, а также непромокаемую обувь, чтобы ноги оставались сухими.";
  } else if (wind > 10) {
    phrase2 = "Поднимается сильный ветер: выбирайте одежду с капюшоном и защитой от продувания.";
  } else if (apparentTemp < temp - 3) {
    phrase2 = "Из-за высокой влажности ощущается холоднее, чем на градуснике, так что утеплитесь получше.";
  } else if (isNight && temp < 15) {
    phrase2 = "Ночная прохлада может быть коварной, так что держите наготове лёгкую кофту.";
  } else {
    phrase2 = "Обувь на плотной подошве отлично защитит от уличной сырости и прохлады в любое время.";
  }

  return `${phrase1} ${phrase2}`;
}